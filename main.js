// snack 1
function somma(a, b) {
    return a + b;
};

console.log(somma(5, 10));

// funzione anonima
const funzioneSommaAnonima = function (a, b) {
    return a + b;
};

console.log(funzioneSommaAnonima(10, 22));

// arrow
const funzioneSommaArrow = (a, b) => a + b;

console.log(funzioneSommaArrow(2, 7));